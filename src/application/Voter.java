package application;

public class Voter {

	private int VoterID;
	private int VoterNIC;
	private String FamilyNumber;
	private String VoterName;
	private String FamilyName;
	private String DOB;
	private String Address;
	private String UserName;
	private String Password;

	/**
	 * @return the voterID
	 */
	public int getVoterID() {
		return VoterID;
	}

	/**
	 * @param voterID the voterID to set
	 */
	public void setVoterID(int voterID) {
		VoterID = voterID;
	}

	/**
	 * @return the voterNIC
	 */
	public int getVoterNIC() {
		return VoterNIC;
	}

	/**
	 * @param voterNIC the voterNIC to set
	 */
	public void setVoterNIC(int voterNIC) {
		VoterNIC = voterNIC;
	}

	/**
	 * @return the familyNumber
	 */
	public String getFamilyNumber() {
		return FamilyNumber;
	}

	/**
	 * @param familyNumber the familyNumber to set
	 */
	public void setFamilyNumber(String familyNumber) {
		FamilyNumber = familyNumber;
	}

	/**
	 * @return the voterName
	 */
	public String getVoterName() {
		return VoterName;
	}

	/**
	 * @param voterName the voterName to set
	 */
	public void setVoterName(String voterName) {
		VoterName = voterName;
	}

	/**
	 * @return the familyName
	 */
	public String getFamilyName() {
		return FamilyName;
	}

	/**
	 * @param familyName the familyName to set
	 */
	public void setFamilyName(String familyName) {
		FamilyName = familyName;
	}

	/**
	 * @return the dOB
	 */
	public String getDOB() {
		return DOB;
	}

	/**
	 * @param dOB the dOB to set
	 */
	public void setDOB(String dOB) {
		DOB = dOB;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return Address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		Address = address;
	}

	/**
	 * @return the userName
	 */
	public String getUserName() {
		return UserName;
	}

	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		UserName = userName;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return Password;
	}

	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		Password = password;
	}
}
